    private void jHandleSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jHandleSubmitMouseClicked
        String name = jName.getText();
        String age = jAge.getText();
        String cpf = jCpf.getText();
        String address = jAddress.getText();
        String phoneNumber = jPhoneNumber.getText();

        if(validateDate(name, age, cpf, address, phoneNumber)){
            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatorios", "Alert", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                int ageConvert = Integer.parseInt(age);
                Study study = new Study(name, ageConvert, cpf, phoneNumber, address);
                this.studyRepository.insert(study);
                JOptionPane.showMessageDialog(null, "Aluno inserido com sucesso");

            }catch(NoResultException ex){
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar aluno", "Alert", JOptionPane.ERROR);
            }
        }

    }//GEN-LAST:event_jHandleSubmitMouseClicked

