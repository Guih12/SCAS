    private void jHandleCloseCashRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jHandleCloseCashRegisterActionPerformed
        Double valueInput = this.cashRegister.getInputValue();
        Double valueOutput = this.cashRegister.getOutputValue();
        Double valueOpen = this.cashRegister.getApertureValue();
       
        Double valueClose = (valueInput + valueOutput) - valueOpen;
        
        try{
            this.cashRegister.setClosingValue(valueClose);
            this.cashRegisterRepostiry.update(cashRegister);
            JOptionPane.showMessageDialog(null, "Caixa fechado com sucesso: " + valueClose + " reais");
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao fechar o caixa", "Alert", JOptionPane.ERROR);
        }
        
    }//GEN-LAST:event_jHandleCloseCashRegisterActionPerformed
    
    
