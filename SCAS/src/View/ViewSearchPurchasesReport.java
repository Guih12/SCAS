     private Date convertDate(String date) throws ParseException{
         Date dateConvert = null;
         try{
            dateConvert = new SimpleDateFormat("dd/MM/yyyy").parse(date);
            System.out.println(dateConvert);
            return dateConvert;
         }catch(ParseException ex){
             System.out.println(ex.getMessage());
         }
        return null;
    }
 
    private void jHadleSubmitPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jHadleSubmitPDFActionPerformed
        
        String dateInit = jDateInit.getText();
        String dateFinal = jDateFinal.getText();
       
        try{
            Date dateInitConvert = this.convertDate(dateInit);
            Date dateFinalConvert = this.convertDate(dateFinal);
            List<Purchase> purchases = this.purchaseRepository.setPurchaseDate(dateInitConvert, dateFinalConvert);
            this.buildReport.createReportPurchase(purchases);
            
        }catch(Exception ex){
            System.out.println(ex.getStackTrace());
        }
       
    }//GEN-LAST:event_jHadleSubmitPDFActionPerformed

